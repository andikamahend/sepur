    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
    String no = txtNo.getText();
    String nim = txtNim.getText();
    String namamhs = txtNama.getText();
    
    if (!nim.matches("\\d+")) {
    JOptionPane.showMessageDialog(this, "NIM harus berupa angka!", "Peringatan", JOptionPane.WARNING_MESSAGE);
    return;
    }

    if (no.isEmpty() || nim.isEmpty() || namamhs.isEmpty()) {
        JOptionPane.showMessageDialog(this,
            "Semua data harus diisi!", "Peringatan", JOptionPane.WARNING_MESSAGE);
        return;
    }

    // Panggil MenuUtama dan kirim data
    MenuUtama menu = new MenuUtama(namamhs, nim, no);
    menu.setVisible(true);
    this.dispose();
    
    }//GEN-LAST:event_jButton1ActionPerformed

