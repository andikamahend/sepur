package tiketkereta.KAJJ;

public class Penumpang {
    public String nama, nik, jenisKelamin, tipePenumpang;
    public int usia;

    public Penumpang(String nama, String nik, String jenisKelamin, String tipePenumpang, int usia) {
        this.nama = nama;
        this.nik = nik;
        this.jenisKelamin = jenisKelamin;
        this.tipePenumpang = tipePenumpang;
        this.usia = usia;
    }
}